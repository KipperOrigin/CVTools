package org.cubeville.cvtools.commands;

public class CommandMapValueException extends RuntimeException {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public CommandMapValueException() {
		super();
	}
	public CommandMapValueException(String message) {
		super(message);
	}
	public CommandMapValueException(String s, Throwable throwable) {
		super(s, throwable);
	}
	public CommandMapValueException(Throwable throwable) {
		super(throwable);
	}

}
